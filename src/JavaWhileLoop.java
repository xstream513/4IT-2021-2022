public class JavaWhileLoop {

    public static void main(String[] args) {
        int i = 0;
        i = i + 1;   // přičítám k i jedničku
        i += 1;      // přičítám k i jedničku
        i -= 1;     // odčítám od i jedničku
        i++;        // přičítám k i jedničku
        i--;        // odčítám od i jedničku

        int j = 0;

        while(j != 3) {
            System.out.println(j);
            j = j + 1;
        }
    }



}
